    public void visualizar() {
        ArrayList<ValeCompras> valeArray = new ArrayList<>();
        DefaultTableModel dtmTabelaValeCompras = (DefaultTableModel) jTableValesComprasExistentes.getModel();
        File arq = new File("dadosValeCompras.txt");
        if (!arq.exists()) {
            System.out.println("Entrou aqui 1");

            try {
                if (arq.createNewFile()) {
                    System.out.println("O arquivo foi criado");
                    System.out.println("Entrou aqui 2");
                } else {
                    System.out.println("O arquivo n√£o foi criado");
                    
                }
            } catch (IOException ex) {
                System.out.println("Entrou aqui 3");
                Logger.getLogger(TelaCadastroValeCompras.class.getName()).log(Level.SEVERE, null, ex);
            }

        }

        //ler arquivo
        try {
System.out.println("Entrou aqui 4");
            FileInputStream fileinput = new FileInputStream(arq);
            ObjectInputStream input = new ObjectInputStream(fileinput);
            //SALVO NO ARRAY O ARQUIVO ORIGINAL
            valeArray = (ArrayList<ValeCompras>) input.readObject();
            input.close();
System.out.println("Entrou aqui 5");
        } catch (EOFException e) {

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(TelaCadastroValeCompras.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(TelaCadastroValeCompras.class.getName()).log(Level.SEVERE, null, ex);
        }
       dtmTabelaValeCompras = (DefaultTableModel) jTableValesComprasExistentes.getModel();
        if (!valeArray.isEmpty()) {
            System.out.println("Entrou aqui 6");
            for (int i = 0; i < valeArray.size(); i++) {
                Object[] dados = {valeArray.get(i).getIdentificacao(), valeArray.get(i).getValor(), valeArray.get(i).getDataVigor(), valeArray.get(i).getDataValidade()};
                dtmTabelaValeCompras.addRow(dados);
                System.out.println("Entrou aqui 7");
            }
        }

    }

